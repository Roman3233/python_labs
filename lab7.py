# Функція для додавання нового запису в розклад
def add_train(train_number, destination, arrival, departure):
    train_schedule[train_number] = {
        "призначення": destination,
        "прибуття": arrival,
        "відправлення": departure
    }
    print(f"Поїзд {train_number} додано до розкладу.")

# Функція для видалення запису з розкладу
def remove_train(train_number):
    if train_number in train_schedule:
        del train_schedule[train_number]
        print(f"Поїзд {train_number} видалено з розкладу.")
    else:
        print(f"Поїзд з номером {train_number} не знайдено.")

def view_sorted_schedule():
    for train_number in sorted(train_schedule.keys()):
        details = train_schedule[train_number]
        print(f"Поїзд {train_number}: {details['призначення']} (прибуття: {details['прибуття']}, відправлення: {details['відправлення']})")

