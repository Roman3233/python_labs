# Функція для перевірки, чи поїзд стоїть на станції у вказаний момент часу
def trains_at_station(hour, minute, schedule):
    trains_on_station = []

    for train in schedule:
        arr_hour, arr_minute = train["прибуття"]
        dep_hour, dep_minute = train["відправлення"]

        # Перевірка, чи вказаний час входить у інтервал перебування поїзда на станції
        if (hour > arr_hour or (hour == arr_hour and minute >= arr_minute)) and \
                (hour < dep_hour or (hour == dep_hour and minute <= dep_minute)):
            trains_on_station.append((train["номер"], train["призначення"]))

    return trains_on_station


